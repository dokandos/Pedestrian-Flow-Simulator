package DataProcessing;

public class SingleDuration{
	int mes;
	int dia;
	int periodo;
	String rol;
	double duracion;
	public SingleDuration(int mes, int dia, int periodo, String rol, double duracion) {
		super();
		this.mes = mes;
		this.dia = dia;
		this.periodo = periodo;
		this.rol = rol;
		this.duracion = duracion;
	}
}
